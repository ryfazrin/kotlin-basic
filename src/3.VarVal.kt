fun main() {
    /**
     * Basic Data Types & Variable
     *
     * var indentifier: Type = initialization
     * var = bisa mengubah nilai
     * val = tidak bisa mengubah nilai
     * */
    var company: String = "Dicoding"
    println(company)

    company = "Ryris"
    println(company)

    val old: Int = 19
    println(old)
}